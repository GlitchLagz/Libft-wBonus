#include "libft.h"

void	ft_putendl_fd(char *s, int fd)
{
	char	nl;
	int		i;

	i = 0;
	nl = '\n';
	while (s[i] != '\0')
		write(fd, &s[i++], 1);
	write(fd, &nl, 1);
}
